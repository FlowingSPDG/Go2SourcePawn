func ReplaceStringName(s *string, typ, rep string) bool {
	if strings.Contains(*s, typ) {
		*s = strings.Replace(*s, typ, rep, -1)
		return true
	}
	return false
}

type TypeString struct {
	TypeName, LhsBracks, Name, RhsBracks string
}

func (ts TypeString) Join(param, is_ref, is_array bool) string {
	if param {
		if is_array {
			if !is_ref {
				/// const type[] name
				/// const type name[N]
				return "const " + ts.TypeName + ts.LhsBracks + " " + ts.Name + ts.RhsBracks
			} else {
				/// type[] name
				/// type name[N]
				return ts.TypeName + ts.LhsBracks + " " + ts.Name + ts.RhsBracks
			}
		} else if is_ref {
			/// type& name
			return ts.TypeName + "& " + ts.Name
		} else {
			/// type name
			return ts.TypeName + " " + ts.Name
		}
	} else {
		if is_array {
			/// type name[N]
			return ts.TypeName + " " + ts.Name + ts.RhsBracks
		} else {
			/// type name
			return ts.TypeName + " " + ts.Name
		}
	}
}

/**
 * SP params -> [const] TypeName ([]... | &) VarName ([N]...)
 * SP vars   ->         TypeName VarName ([(N)]...)
 */
func GetTypeString(expr ast.Expr, name string, param bool) string {
	var ts TypeString
	ts.Name = name
	var is_ref, is_array bool
	if typ := SrcGo_ASTMod.ASTCtxt.SrcGoTypeInfo.TypeOf(expr); typ != nil {
	recheck:
		original := typ.String()
		type_name := strings.Replace(original, "untyped ", "", -1)
		if _, found := TypeNames[type_name]; found {
			type_name = TypeNames[type_name]
		}
		type_name = strings.TrimFunc(type_name, func(r rune) bool {
			return !unicode.IsLetter(r)
		})
		ts.TypeName = type_name
		switch t := typ.(type) {
			//case *types.Struct:
			case *types.Array:
				ts.RhsBracks = fmt.Sprintf("[%d]", t.Len()) + ts.RhsBracks
				is_array = true
				typ = t.Elem()
				goto recheck
			case *types.Named:
				if _, is_struct := t.Underlying().(*types.Struct); !is_struct {
					if _, builtin := SrcGo_ASTMod.ASTCtxt.BuiltnTypes[type_name]; !builtin {
						typ = t.Underlying()
						goto recheck
					}
				}
			case *types.Slice:
				typ = t.Elem()
				if param {
					ts.LhsBracks += "[]"
				} else {
					ts.RhsBracks += "[]"
				}
				is_array = true
				goto recheck
			case *types.Pointer:
				if !is_ref {
					is_ref = true
				} else {
					SrcGo_ASTMod.PrintSrcGoErr(expr.Pos(), "Multi-Pointers are Illegal.")
				}
				typ = t.Elem()
				goto recheck
			case *types.Basic:
				if type_name=="string" {
					ts.TypeName = "char"
					if param {
						ts.LhsBracks += "[]"
					} else {
						ts.RhsBracks += "[]"
					}
					is_array = true
				}
		}
	}
	return ts.Join(param, is_ref, is_array)
}


func WriteParams(flist *ast.FieldList) []string {
	param_list := make([]string, 0)
	for _, parm := range flist.List {
		for _, name := range parm.Names {
			param_list = append(param_list, GetTypeString(parm.Type, name.Name, true))
		}
	}
	return param_list
}

func WriteStructMembs(flist *ast.FieldList) []string {
	field_list := make([]string, 0)
	for _, field := range flist.List {
		for _, member_name := range field.Names {
			field_str := GetTypeString(field.Type, member_name.Name, false)
			field_list = append(field_list, field_str)
		}
	}
	return field_list
}


/// final code generation stuffs.
type CodeGen struct {
	Includes, Globals, Funcs strings.Builder
	Tabs uint
}

type CodeBlock struct {
	Tab uint
	Stmts []string
}

type CodeFunc struct {
	CodeBlock
	Header string
}

func GenSPFile(f *ast.File) string {
	var c = CodeGen{}
	c.Includes.WriteString(Header + SrcModInc)
	for _, d := range f.Decls {
		switch decl := d.(type) {
			case *ast.GenDecl:
				c.GenerateGenDecl(decl)
			
			//case *ast.FuncDecl:
			//	c.GenerateFuncDecl(decl)
		}
	}
	return c.Includes.String() + "\n" + c.Globals.String() + "\n" + c.Funcs.String()
}

func (c *CodeGen) GenerateGenDecl(g *ast.GenDecl) {
	switch g.Tok {
		case token.IMPORT:
			for _, spec := range g.Specs {
				imp := spec.(*ast.ImportSpec)
				if imp.Path.Value[1] == '.' {
					c.Includes.WriteString( strings.Replace(IncludeTemplate, "<PATH>", "\"" + imp.Path.Value[2:], -1) )
					c.Includes.WriteString("\n")
				} else {
					c.Includes.WriteString( strings.Replace(IncludeTemplate, "PATH", imp.Path.Value[1 : len(imp.Path.Value)-1], -1) )
					c.Includes.WriteString("\n")
				}
			}
		
		case token.CONST:
			for _, spec := range g.Specs {
				const_spec := spec.(*ast.ValueSpec)
				/// if a constant is untyped, it can have different names and associating values.
				if const_spec.Type != nil {
					for _, name := range const_spec.Names {
						/// TODO: rework this to generate values from a different function.
						for _, value := range const_spec.Values {
							switch val := value.(type) {
								case *ast.CompositeLit:
									c.Globals.WriteString(GetTypeString(const_spec.Type, name.Name, false))
									c.Globals.WriteString(" = {\n")
									for n, expr := range val.Elts {
										c.Globals.WriteString("\t" + GetExprString(expr))
										if n+1 != len(val.Elts) {
											c.Globals.WriteString(",")
										}
										c.Globals.WriteString("\n")
									}
									c.Globals.WriteString("};\n")
									
								default:
									c.Globals.WriteString(GetTypeString(const_spec.Type, name.Name, false))
									c.Globals.WriteString(" = " + GetExprString(value) + ";\n")
							}
						}
					}
				} else {
					for i, name := range const_spec.Names {
						for _, value := range const_spec.Values {
							switch val := value.(type) {
								case *ast.CompositeLit:
									c.Globals.WriteString(GetTypeString(val.Type, name.Name, false))
									c.Globals.WriteString(" = {\n")
									for n, expr := range val.Elts {
										c.Globals.WriteString("\t" + GetExprString(expr))
										if n+1 != len(val.Elts) {
											c.Globals.WriteString(",")
										}
										c.Globals.WriteString("\n")
									}
									c.Globals.WriteString("};\n")
									
								default:
									c.Globals.WriteString(GetTypeString(const_spec.Values[i], name.Name, false))
									c.Globals.WriteString(" = " + GetExprString(value) + ";\n")
							}
						}
					}
				}
			}
			c.Globals.WriteString("\n")
			
		case token.VAR:
		/**
			Names   []*Ident      // value names (len(Names) > 0)
			Type    Expr          // value type; or nil
			Values  []Expr        // initial values; or nil
		 */
			for _, spec := range g.Specs {
				var_spec := spec.(*ast.ValueSpec)
				/// if a constant is untyped, it can have different names and associating values.
				if var_spec.Type != nil {
					for _, name := range var_spec.Names {
						c.Globals.WriteString(GetTypeString(var_spec.Type, name.Name, false))
						for _, value := range var_spec.Values {
							switch val := value.(type) {
								case *ast.CompositeLit:
									c.Globals.WriteString(" = {")
									for n, expr := range val.Elts {
										c.Globals.WriteString("\t" + GetExprString(expr))
										if n+1 != len(val.Elts) {
											c.Globals.WriteString(",")
										}
										c.Globals.WriteString("\n")
									}
									c.Globals.WriteString("}")
								default:
									c.Globals.WriteString(" = " + GetExprString(value))
							}
						}
						c.Globals.WriteString(";\n")
					}
				} else {
					for _, name := range var_spec.Names {
						for _, value := range var_spec.Values {
							c.Globals.WriteString(GetTypeString(value, name.Name, false))
							switch val := value.(type) {
								case *ast.CompositeLit:
									c.Globals.WriteString(" = {\n")
									for n, expr := range val.Elts {
										c.Globals.WriteString("\t" + GetExprString(expr))
										if n+1 != len(val.Elts) {
											c.Globals.WriteString(",")
										}
										c.Globals.WriteString("\n")
									}
									c.Globals.WriteString("}")
								default:
									c.Globals.WriteString(" = " + GetExprString(value))
							}
						}
						c.Globals.WriteString(";\n")
					}
				}
			}
			c.Globals.WriteString("\n")
		
		case token.TYPE:
			for _, spec := range g.Specs {
				type_spec := spec.(*ast.TypeSpec)
				switch t := type_spec.Type.(type) {
					case *ast.Ident:
						TypeNames[type_spec.Name.Name] = t.Name
					
					case *ast.StructType:
						c.Globals.WriteString("enum struct " + type_spec.Name.Name + " {")
						field_list := WriteStructMembs(t.Fields)
						for _, field := range field_list {
							c.Globals.WriteString("\n\t" + field + ";")
						}
						c.Globals.WriteString("\n}\n")
					
					case *ast.FuncType:
						/// typedef Whatever = function type (params);
						c.Globals.WriteString("typedef " + type_spec.Name.Name + " = function ")
						if t.Results != nil {
							type_str := GetTypeString(t.Results.List[0].Type, "", false)
							type_str = strings.TrimSpace(type_str)
							if strings.Count(type_str, "[") > 0 {
								/// shoot error but continue.
								SrcGo_ASTMod.PrintSrcGoErr(t.Pos(), "Typedef'd functions are not allowed to return arrays.")
							}
							c.Globals.WriteString(type_str)
						} else {
							c.Globals.WriteString("void")
						}
						c.Globals.WriteString(" (")
						
						param_list := WriteParams(t.Params)
						param_count := len(param_list)
						for i, param := range param_list {
							c.Globals.WriteString(param)
							if i+1 != param_count {
								c.Globals.WriteString(", ")
							}
						}
						c.Globals.WriteString(");\n")
				}
			}
			c.Globals.WriteString("\n")
		//case token.VAR:
	}
}

func GetExprString(e ast.Expr) string {
	switch x := e.(type) {
		case *ast.IndexExpr:
			return GetExprString(x.X) + "[ " + GetExprString(x.Index) + "]"
		
		case *ast.KeyValueExpr:
			return GetExprString(x.Key) + " = " + GetExprString(x.Value)
		
		case *ast.ParenExpr:
			return "(" + GetExprString(x.X) + ")"
		
		case *ast.StarExpr:
			/// ignore deref star
			return GetExprString(x.X)
		
		case *ast.UnaryExpr:
			if x.Op==token.AND {
				return GetExprString(x.X)
			}
			return x.Op.String() + GetExprString(x.X)
		
		case *ast.CallExpr:
			var call string
			name := GetExprString(x.Fun)
			if _, found := IdenNames[name]; found {
				name = TypeNames[name]
			}
			if obj, found := SrcGo_ASTMod.ASTCtxt.StrDefs[name]; found && obj != nil {
				if _, is_fptr := obj.(*types.Var); is_fptr {
					call += fmt.Sprintf("Call_StartFunction(null, %s); ", name)
					for i, arg := range x.Args {
						if typ := SrcGo_ASTMod.ASTCtxt.SrcGoTypeInfo.TypeOf(arg); typ != nil {
							switch t := typ.(type) {
								case *types.Array:
									id := GetExprString(arg)
									switch t.Elem().String() {
										case "char":
											call += fmt.Sprintf("Call_PushStringEx(%s, sizeof(%s), SM_PARAM_COPYBACK); ", id, id)
										default:
											call += fmt.Sprintf("Call_PushArrayEx(%s, sizeof(%s), SM_PARAM_COPYBACK); ", id, id)
									}
								case *types.Pointer:
									switch t.Elem().String() {
										case "float":
											call += fmt.Sprintf("Call_PushFloatRef(%s); ", GetExprString(arg))
										default:
											call += fmt.Sprintf("Call_PushCellRef(%s); ", GetExprString(arg))
									}
								case *types.Basic:
									switch t.Name() {
										case "string":
											call += fmt.Sprintf("Call_PushString(%s); ", GetExprString(arg))
										case "float", "float32":
											call += fmt.Sprintf("Call_PushFloat(%s); ", GetExprString(arg))
										default:
											call += fmt.Sprintf("Call_PushCell(%s); ", GetExprString(arg))
									}
							}
						}
						if i+1 != len(x.Args) {
							call += ", "
						}
					}
				}
			} else {
				call += name + "("
				for i, arg := range x.Args {
					call += GetExprString(arg)
					if i+1 != len(x.Args) {
						call += ", "
					}
				}
				call += ")"
			}
			return call
		
		case *ast.BinaryExpr:
			return GetExprString(x.X) + " " + x.Op.String() + " " + GetExprString(x.Y)
		
		case *ast.SelectorExpr:
			return GetExprString(x.X) + "." + GetExprString(x.Sel)
		
		case *ast.Ident:
			if n, found := IdenNames[x.Name]; found {
				return n
			}
			return x.Name
		
		case *ast.BasicLit:
			return x.Value
		
		/// TODO: make as 'view_as< type >(expr)' ?
		/// TODO: make new, local array when slicing?
		case *ast.TypeAssertExpr, *ast.SliceExpr:
			return ""
	}
	return ""
}